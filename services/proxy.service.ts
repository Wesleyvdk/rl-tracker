let allProxies: string[] = [];

export async function getRandomProxy(): Promise<string | null> {
    if (allProxies.length === 0) {
        allProxies = [...hardcodedProxies];
        // Ensure .env proxies are also included if configured
        const envProxies = process.env.PROXIES?.split(",").map((p: string) => p.trim()).filter(Boolean) || [];
        const formattedEnvProxies = envProxies.map((p: string) => p.startsWith('http') ? p : `http://${p}`);
        allProxies.push(...formattedEnvProxies);
    }

    if (allProxies.length === 0) return null;

    // Pick a random proxy
    const randomIndex = Math.floor(Math.random() * allProxies.length);
    return allProxies[randomIndex];
}
